package main;

public class MyPair {

	private int first = 0, second = 0;
	
	public MyPair(){}
	
	public void setFirst(int first) {
		this.first = first;
	}
	
	public void setSecond(int second) {
		this.second = second;
	}

	public int getFirst() {
		return first;
	}

	public int getSecond() {
		return second;
	}
	
	
}
